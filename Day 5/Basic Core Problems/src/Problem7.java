public class Problem7 {
    //Swap two numbers
    public static void main(String[] args) {
        int a=10;
        int b=15;
        System.out.println("Before Swapping");
        System.out.println("Value of a is: "+a+" Value of b is: "+b);
        int temp=a;
        a=b;
        b=temp;
        System.out.println("After Swapping");
        System.out.println("Value of a is: "+a+" Value of b is: "+b);
        }
}
